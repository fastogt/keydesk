function svgIcon(paths: string): string {
  return `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor">${paths}</svg>`;
}

function renderLink(item: { id: string; label: string; href: string; icon: string }, activePage: string): string {
  const cls = item.id === activePage ? 'sidebar-link active' : 'sidebar-link';
  return `<a href="${item.href}" class="${cls}">${svgIcon(item.icon)}<span>${item.label}</span></a>`;
}

export function initSidebar(activePage: string): void {
  const container = document.getElementById('sidebar');
  if (!container) return;

  const navHtml = NAV_ITEMS.map(item => renderLink(item, activePage)).join('');
  const footerHtml = FOOTER_ITEMS.map(item => renderLink(item, activePage)).join('')
    + `<a href="#" class="sidebar-link" id="sidebarLogout">${svgIcon('<path stroke-linecap="round" stroke-linejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0 0 13.5 3h-6a2.25 2.25 0 0 0-2.25 2.25v13.5A2.25 2.25 0 0 0 7.5 21h6a2.25 2.25 0 0 0 2.25-2.25V15m3 0 3-3m0 0-3-3m3 3H9" />')}<span>Logout</span></a>`;

  container.innerHTML =
    `<a href="/dashboard" class="sidebar-brand">KeyDesk</a>` +
    `<nav class="sidebar-nav">${navHtml}</nav>` +
    `<div class="sidebar-footer">${footerHtml}</div>`;

  document.getElementById('sidebarLogout')?.addEventListener('click', (e) => {
    e.preventDefault();
    clearAuth();
    window.location.href = '/setup';
  });

  const toggle = document.getElementById('sidebarToggle');
  if (toggle) {
    toggle.addEventListener('click', () => container.classList.toggle('open'));
    document.addEventListener('click', (e) => {
      if (!container.contains(e.target as Node) && e.target !== toggle) {
        container.classList.remove('open');
      }
    });
  }
}
